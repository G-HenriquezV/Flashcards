function showCreateDeckForm() {
    let x = document.getElementById("deckCreateForm")
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function showCardAnswer() {
    let x = document.getElementById("cardAnswer")
    if (x.style.display === "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}